alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

def cypher(text,shift,direction):
    output=""
    if direction == "decode":
        shift *= -1
    for n in range(0,len(text)):
        if text[n] in alphabet:
            output+=alphabet[(alphabet.index(text[n]) + shift) % len(alphabet)]
        else:
            output+=text[n]
    return output

